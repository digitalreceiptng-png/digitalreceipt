  totalRow: { flexDirection: 'row', justifyContent: 'space-between', alignItems: 'center' },
  totalLabel: { fontSize: 16, fontWeight: '800', color: '#111827' },
  totalAmt: { fontSize: 18, fontWeight: '900', color: '#111827' },
